    public static String C64StrFromBytes(byte[] inBytes){
        String response = "";
        for (byte b: inBytes) {
            if ((b & 0xff) <= 0x7f) {
                response += C64CHAR[(b & 0xff)];
            } else {
                response += (char)(b & 0xff);
            }
        }
        return response;
    }
    
